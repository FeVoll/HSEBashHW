#!/bin/bash

# запускаем три команды sleep в фоне с разными интервалами
echo "запускаем фоновые процессы..."
sleep 5 &
sleep 10 &
sleep 15 &

# выводим список фоновых задач
echo "фоновые процессы:"
jobs

# демонстрируем использование fg и bg
echo "переводим первую задачу в передний план..."
fg %1

echo "переводим задачу обратно в фон..."
sleep 5 & # перезапускаем, чтобы не мешать, если процесс завершён
bg %1

# проверяем обновлённый список фоновых задач
echo "обновлённый список фоновых процессов:"
jobs

# запускаем ещё одну команду в фоне для демонстрации
echo "запускаем дополнительный процесс..."
sleep 20 &
jobs

# финальный вывод для пользователя
read -p "нажмите enter, чтобы закрыть окно."
